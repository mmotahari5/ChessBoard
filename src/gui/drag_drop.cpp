std::pair<int, int> StackPage::positionExtraction(std::string input){
	std::pair<int, int> pair;
	char ch = input[0], nm = input[1];
 	ch -= 49;
	std::string str1, str2;
	str1 += ch;
	str2 += nm;
	pair.first = std::stoi(str1);
	pair.second = (std::stoi(str2) - 8) * (-1);

	return pair;
}
