void StackPage::on_1_drag_data_received(const Glib::RefPtr<Gdk::DragContext>& context, int x, int y, const Gtk::SelectionData& selection_data, guint info, guint time){
	int i = 1;
	cellDestination = positionOfSquars[i];
	if(motionVerification()){
		
		pBoardGame->remove(*pointerPiece);
		pBoardGame->remove(*(squars[i]));

		pBoardGame->attach(*(pointerPiece), positionExtraction(positionOfSquars[i]).first, positionExtraction(positionOfSquars[i]).second);
		pBoardGame->attach(*(squars[i]), positionExtraction(positionOfPieces[piece]).first, positionExtraction(positionOfPieces[piece]).second);

		positionOfSquars[i] = positionOfPieces[piece];
		positionOfPieces[piece] = cellDestination;
	}
	context->drag_finish(false, false, time);
}
